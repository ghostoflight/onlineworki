def name(lang):
    """Native display name of a language (for the picker)."""
    return LANG_NAMES.get(lang, lang)


def lookup(key, lang=DEFAULT_LANG, **kwargs):
    """Translate key → text. Fallback: lang → English → key itself."""
    table = LOCALES.get(lang) or LOCALES[DEFAULT_LANG]
    s = table.get(key)
    if s is None:
        s = LOCALES[DEFAULT_LANG].get(key, key)
    if kwargs:
        try:
            s = s.format(**kwargs)
        except (KeyError, IndexError, ValueError):
            pass
    return s
